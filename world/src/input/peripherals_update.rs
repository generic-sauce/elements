impl From<char> for Character {
	fn from(c: char) -> Self {
		if !c.is_ascii_control() {
			Character::Char(c)
		} else if c as u8 == 8 {
			Character::Backspace
		} else if c as u8 == 127 {
			Character::Delete
		} else {
			// println!("unknown char: {}", c as u32);
			Character::Unknown
		}
	}
}

